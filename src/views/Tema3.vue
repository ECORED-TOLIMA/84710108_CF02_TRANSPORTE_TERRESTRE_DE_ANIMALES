      h2 3.8 Técnicas de aspersión a animales en pie
    
    p.mb-5 Las técnicas de aspersión se refieren al uso de métodos de rociado o pulverización de agua sobre los animales en pie durante el transporte para ayudar a controlar el estrés térmico (hipertermia). Esta técnica es un método de emergencia o de manejo en climas cálidos para mitigar el riesgo de golpe de calor, especialmente cuando la temperatura supera los (debe estar entre 5°C y 30°C).

